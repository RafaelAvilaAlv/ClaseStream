    public void gestionarinformededatos(ArrayList<Persona> auxi) {

        String[] cabezeras
                = {
                    "Nombre",
                    "Apellido 1",
                    "Apellido 2"
                };

        model.setColumnIdentifiers(cabezeras);
        model.setRowCount(0);

        auxi.stream().map(historial -> {
            Object[] datos = new Object[3];
            datos[0] = historial.getNombre();
            datos[1] = historial.getApellido1();
            datos[2] = historial.getApellido2();
            return datos;
        }).forEach(model::addRow);
        tbldatospersona.setModel(model);

    }

