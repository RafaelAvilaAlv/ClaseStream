    public void Buscarpersonas(String auxBuscar) {
        int opcion = jcmbopdat.getSelectedIndex();
       
        //Aqui provcedemos a utlizar el metodo filter se asigna mediante una funcion lambda
        //validara  cada uno de los datos escogidos por el usuario
        if (opcion == 0) {
            busqueda = (ArrayList<Persona>) menuprincipal.listPersona.stream()
                    .filter(persona -> persona.getNombre().equalsIgnoreCase(auxBuscar)).collect(Collectors.toList());
        }
        if (opcion == 1) {
            busqueda = (ArrayList<Persona>) menuprincipal.listPersona.stream()
                    .filter(persona -> persona.getApellido1().equalsIgnoreCase(auxBuscar)).collect(Collectors.toList());
        }
        if (opcion == 2) {
            busqueda = (ArrayList<Persona>) menuprincipal.listPersona.stream()
                    .filter(persona -> persona.getApellido2().equalsIgnoreCase(auxBuscar)).collect(Collectors.toList());
        }

    }

